<?php

/* ***** OPERADORES DE INCREMENTO E DECREMENTO ***** */
$a = 10;

echo $a++ . "<br>";//incremento depois da operação
echo ++$a . "<br>";//incremento antes da operação
echo $a . "<br>";

echo $a-- . "<br>";//decremento depois da operação
echo --$a . "<br>";//decremento antes da operação
echo $a . "<br>";